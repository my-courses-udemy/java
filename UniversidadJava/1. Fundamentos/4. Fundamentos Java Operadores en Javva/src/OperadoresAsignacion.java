
public class OperadoresAsignacion {
    
    public static void main(String args[]){
    
        int a = 3, b = 2;
        int c = a + 5 - b; // se evalua, realiza sus operaciones y se asigna a C
        System.out.println("c = " + c);
    
        a += 1; // a = a + 1;
        System.out.println("a = " + a);
        
    }
}
