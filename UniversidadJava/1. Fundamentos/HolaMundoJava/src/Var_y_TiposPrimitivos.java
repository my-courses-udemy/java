
public class Var_y_TiposPrimitivos {
    
    public static void main(String args[]){
        
        var numeroEntero = 10;
        System.out.println("numeroEntero = " + numeroEntero);
        
        var numDouble = 12.0; // indicamos que es de tipo double
        System.out.println("numDouble = " + numDouble);
        
        var numFloat = 10.0F; // indicamos que es de tipo flotante
        System.out.println("numFloat = " + numFloat);
        
    }
    
}
